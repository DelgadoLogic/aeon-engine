Write-Host ""
Write-Host "  To run:"
Write-Host "    .\publish\$Tier\$exeName"
Write-Host ""
Write-Host "  To build installer (LogicFlow):"
Write-Host '    cd ..\LogicFlow\Installer; .\build_installer.ps1'
