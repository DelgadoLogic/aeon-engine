# 7. Summary
# ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
Step "7/7" "Build complete"
$elapsed = [math]::Round(((Get-Date) - $StartTime).TotalSeconds, 1)
$outSize = [math]::Round((Get-ChildItem $publishDir -Recurse |
    Measure-Object Length -Sum).Sum / 1MB, 2)

